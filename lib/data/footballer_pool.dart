  /// Returns the pooled attributes for a player [name], or null if unknown.
  ///
  /// Matching is case-insensitive and tolerant of partial matches so that a
  /// query like "messi" resolves to "Lionel Messi".
  static Player? lookup(String name) {
    final key = name.toLowerCase().trim();
    final exact = _byName[key];
    if (exact != null) return exact;

    for (final entry in _byName.entries) {
      if (key.contains(entry.key) || entry.key.contains(key)) {
        return entry.value;
      }
    }
    return null;
  }

